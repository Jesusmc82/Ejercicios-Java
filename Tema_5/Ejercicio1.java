//package ejercicio1;

public class Ejercicio1 {
    public static void main(String[] args) {
        System.out.println("vamos a mostrarle los números multiplos de 5 desde el 0 al 100:");

        for (int i = 0; i <= 100; i = i + 5) {
            System.out.println(i);

        }

    }

}
