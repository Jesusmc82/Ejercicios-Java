/**
 * Dos columnas, una en inglés y su traducción en español.
 *
 * @author Jesus Martin Castro
 */

public class Ejercicio03 {
  public static void main(String[] args) {
    System.out.println("Ingles             Español");
    System.out.println("--------------------------");
    System.out.printf("%-10s      %10s\n",  "Father", "Padre");
    System.out.printf("%-10s      %10s\n",  "Mother", "Madre");
    System.out.printf("%-10s      %10s\n",  "Daughter", "Hija");
    System.out.printf("%-10s      %10s\n",  "Son", "Hijo");
    System.out.printf("%-10s      %10s\n",  "Brother", "Hermano");
    System.out.printf("%-10s      %10s\n",  "Sister", "Hermana");
    System.out.printf("%-10s      %9s\n",  "Grandmother", "Abuela");
    System.out.printf("%-10s      %9s\n",  "Grandfather", "Abuelo");
    System.out.printf("%-10s      %10s\n",  "Aunt", "Tia");
    System.out.printf("%-10s      %10s\n",  "Uncle", "Tio");
  }
}
